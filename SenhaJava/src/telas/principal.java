private void cadastraSenha(int numero) {
	conexao banco = new conexao();
	try {
		String sql = "INSERT INTO tb01_senhas\n" + "(tb01_numero, tb01_chamado)\n" + "VALUES (" + (numero + 1) + ", 'n')";
		banco.conectar();
		int rs = statement.executeUpdate(sql);
		System.out.println(sql);
		verBanco();
		banco.desconectar();
	} catch (Exception e) {
		System.out.println(e.getMessage());
	}
}
private void verBanco() {
	conexao banco = new conexao();
	try {
		String sql = "SELECT MAX(tb01_numero)AS max_numero from tb01_senhas";
		banco.conectar();
		ResultSet rs = statement.executeQuery(sql);
		if(rs.next()) {
			cont = rs.getInt("max_numero");
			lblsenha.setVisible(true);
			lblsenha.setText(String.valueOf(cont));
			System.out.println(cont);
		} else {
			System.out.println("NÃ£o foram encontrados resultados.");
		}
		banco.desconectar();
	} catch (Exception e) {
		System.out.println(e.getMessage());
	}
}
void atualiza() {
	verBanco();
}
