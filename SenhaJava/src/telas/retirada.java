private void verBanco() {
	conexao banco = new conexao();
	try {
		String sql = "SELECT MIN(tb01_numero)AS min_numero from tb01_senhas";
		sql += "	WHERE tb01_chamado = 'n'";
		banco.conectar();
		ResultSet rs = statement.executeQuery(sql);
		if(rs.next()) {
			cont = rs.getInt("min_numero");
			lblsenha.setVisible(true);
			if(cont == 0) {
				lblsenha.setText("Sem senhas para serem chamadas");
			} else {
				lblsenha.setText(String.valueOf(cont));
			}
			System.out.println(cont);
		} else {
			System.out.println("NÃ£o foram encontrados resultados.");
		}
		banco.desconectar();
	} catch (Exception e) {
		System.out.println(e.getMessage());
	}
}
private void atualizaSenha() {
	conexao banco = new conexao();
	try {
		String sql = "UPDATE tb01_senhas\n" + "	SET\n" 
                        + "tb01_chamado='s'\n" 
                        + "WHERE tb01_numero=" + cont;
		banco.conectar();
		int rs = statement.executeUpdate(sql);
		verBanco();
		banco.desconectar();
	} catch (Exception e) {
		System.out.println(e.getMessage());
	}
}
